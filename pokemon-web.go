package main

import (
	"github.com/ghargrove/pokemon-web/http/rest"
	"github.com/ghargrove/pokemon-web/internal"
	"github.com/gin-gonic/gin"
	"github.com/joho/godotenv"
	"log"
)

var schema string = `
CREATE TABLE IF NOT EXISTS kids (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	name VARCHAR NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS pokemon (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	name VARCHAR NOT NULL,
	PRIMARY KEY(id)
);

CREATE TABLE IF NOT EXISTS cards (
	id INT GENERATED BY DEFAULT AS IDENTITY,
	tcg_id VARCHAR NOT NULL,
	pokemon_id INT NOT NULL,
	PRIMARY KEY(id),
	CONSTRAINT fk_pokemon FOREIGN KEY(pokemon_id) REFERENCES pokemon(id)
);

CREATE TABLE IF NOT EXISTS cards_kids (
	card_id INT NOT NULL,
	kid_id INT NOT NULL,
	CONSTRAINT fk_card FOREIGN KEY(card_id) REFERENCES cards(id),
	CONSTRAINT fk_kid FOREIGN KEY(kid_id) REFERENCES kids(id)
)
`

func main() {
	err := godotenv.Load()
	if err != nil {
		log.Fatal("Error loading env file")
	}

	db := internal.CreateDB()
	db.MustExec(schema)

	r := gin.Default()

	r.GET("/api/sets", rest.HandleAllSets)
	r.GET("/api/sets/:id", rest.HandleSingleSet)
	r.GET("/api/sets/:id/cards", rest.HandleCardsForSet)
	r.GET("/api/kids", func(c *gin.Context) {
		rest.HandleAllKids(c, db)
	})
	r.GET("/api/kids/:id", func(c *gin.Context) {
		rest.HandleIndividualKid(c, db)
	})
	r.POST("/api/kids/:id/cards", func(c *gin.Context) {
		rest.CreateKidCard(c, db)
	})
	r.Run(":8082")
}
